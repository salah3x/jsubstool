    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
	//redirect system output to textArea
	
	if(this.jRadioButton3.isSelected())
		this.vidFormat=this.jTextField1.getText();
	if(this.jRadioButton6.isSelected())
		this.subsFormat=this.jTextField2.getText();
	this.path=this.jTextField3.getText();
	if(this.jRadioButton9.isSelected())
		this.epFormat=this.jTextField4.getText();
	if(this.subsFormat.contains("/")){
		String help[]=this.subsFormat.split("/");
		MyClass m=new MyClass(this.path, this.vidFormat, help[0], this.epFormat, this.numEp, this.mkdir, this.output);
		MyClass m2=new MyClass(this.path, this.vidFormat,help[1] , this.epFormat, this.numEp, this.mkdir, this.output);
		m.rename();
		m2.rename();
	}
	else{
		MyClass m=new MyClass(this.path, this.vidFormat, this.subsFormat, this.epFormat, this.numEp, this.mkdir, this.output);
		m.rename();
	}
    }//GEN-LAST:event_jButton2ActionPerformed

