    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // show file dialog
        JFileChooser fc = new JFileChooser();
	fc.setCurrentDirectory(new File("."));
        fc.setDialogTitle("Choose your videos' folder");
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fc.setAcceptAllFileFilterUsed(false);
        int resp = fc.showOpenDialog(this.jPanel1);
       if(resp == JFileChooser.APPROVE_OPTION){
		   this.path=fc.getSelectedFile().getAbsolutePath();
		this.jTextField3.setText(this.path);
		if(this.output)
			this.jTextArea1.append("Setting changed: path = "+this.path+"\n");
	 }
	 else{
		   if(this.output)
			   this.jTextArea1.append("Open folder cancled \n");
	   }
    }//GEN-LAST:event_jButton1ActionPerformed

